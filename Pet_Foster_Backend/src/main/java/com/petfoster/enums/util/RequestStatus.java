package com.petfoster.enums.util;
/**
 * Enum representing the status of a foster request.
 * The possible statuses are:
 * <ul>
 *   <li>PENDING: The request is awaiting approval or rejection.</li>
 *   <li>ACCEPTED: The request has been approved.</li>
 *   <li>REJECTED: The request has been denied.</li>
 * </ul>
 */
public enum RequestStatus {
	PENDING, ACCEPTED, REJECTED
}
